            <h2 >Sistemas Operativos</h2>
            <p>En el primer capitulo, se hablará de las diferentes administraciones del sistema operativo </p>
            <p class="preguntas"><b>¿Que es S.O?</b><br></p> <p>Es la parte del sistema de cómputo que administra el hardware y software. En otras palabras,
                controla los archivos, dispositivos, secciones de la memoria principal y cada nanonno segundo del tiempo 
                de procesamiento; asimismo, controla quien puede utilizar el sistema y de que manera.

            <p class="preguntas"> <b> Tipos de adminsitradores</b><br>
            <p> *Administrador de dispositivos <br>
                *Administrador de Procesador<br>
                *Administrador de la memoria<br>
                *Administrador de archivos<br>
                *Administrador de red<br>  </p>
            <p> Todos tienen como objetivo, monitorear continuamente los recursos,
                obligar al cumplimiento de las politicas que determinan quien obtiene 
                qué, cuándo y cuánto.<br>Asignar los recursos cuando es apropiado y por último
                liberar espacio - recuperarlo - cuando es conveniente.
            </p>
