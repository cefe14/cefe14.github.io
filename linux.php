        <h1>Informacion sobre Linux</h1>
        <h3>Comienzos</h3>
        <p>Linux es una familia de sistemas operativos de código abierto tipo Unix basados en el kernel de Linux,un kernel de sistema operativo lanzado por primera vez el 17 de septiembre de 1991 por Linus Torvalds. Linux normalmente se empaqueta en una distribución de Linux.

        Las distribuciones incluyen el kernel de Linux y el software y las bibliotecas del sistema de soporte, muchas de las cuales son proporcionadas por el Proyecto GNU. Muchas distribuciones de Linux usan la palabra "Linux" en su nombre, pero la Free Software Foundation usa el nombre "GNU/Linux" para enfatizar la importancia del software GNU, causando cierta controversia. 

        Las distribuciones populares de Linux incluyen Debian, Fedora Linux y Ubuntu. Las distribuciones comerciales incluyen Red Hat Enterprise Linux y SUSE Linux Enterprise. Las distribuciones de Linux de escritorio incluyen un sistema de ventanas como X11 o Wayland, y un entorno de escritorio como GNOME o KDE Plasma. Las distribuciones destinadas a servidores pueden omitir gráficos por completo o incluir una pila de soluciones como LAMP. Debido a que Linux es libremente redistribuible, cualquiera puede crear una distribución para cualquier propósito.</p>
